package Ch10_StringBuffer;

public class Demo04_ReplaceMethod {

	public static void main(String[] args) {
		
		// Replace () is used to replace old to new String.
		
		String s1 = "Javed is a Developer and Javed is a good Person";
		String s2 = s1.replace("Javed", "Rahul");
		System.out.println(s2);

	}

}
